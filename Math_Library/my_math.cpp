#include "my_math.h"
namespace my_math {
    float add(float a, float b) {
        return a + b;
    }

    float sub(float a, float b) {
        return a - b;
    }

    float multiply(float a, float b) {
        return a * b;
    }

    float divide(float a, float b) {
        return a / b;
    }
}